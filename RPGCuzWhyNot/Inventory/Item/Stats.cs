namespace RPGCuzWhyNot.Inventory.Item {
	public class Stats {
		public int speed;
		public int strength;
		public int accuracy;
		public int fortitude;

		public Stats(int speed, int strength, int accuracy, int fortitude) {
			this.speed = speed;
			this.strength = strength;
			this.accuracy = accuracy;
			this.fortitude = fortitude;
		}
	}
}